package pageReplace;

public class Real {
	private int realPage;
	private int virtualPage;
	private int State;
	public int getRealPage() {
		return realPage;
	}
	public void setRealPage(int realPage) {
		this.realPage = realPage;
	}
	public int getVirtualPage() {
		return virtualPage;
	}
	public void setVirtualPage(int virtualPage) {
		this.virtualPage = virtualPage;
	}
	public int getState() {
		return State;
	}
	public void setState(int state) {
		State = state;
	}
	
	
}
